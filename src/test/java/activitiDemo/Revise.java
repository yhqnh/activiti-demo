package activitiDemo;

import java.io.Serializable;

public class Revise implements Serializable{
	
	private String manager;
	
	private String largeare;
	
	private String majordomo;

	public String getManager() {
		return manager;
	}

	public void setManager(String manager) {
		this.manager = manager;
	}

	public String getLargeare() {
		return largeare;
	}

	public void setLargeare(String largeare) {
		this.largeare = largeare;
	}

	public String getMajordomo() {
		return majordomo;
	}

	public void setMajordomo(String majordomo) {
		this.majordomo = majordomo;
	}
	
	
}
